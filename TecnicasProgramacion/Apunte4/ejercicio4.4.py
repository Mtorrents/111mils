
romano=str(input("Ingrese un numero romano: "))

if romano=="I":
    print("Vale 1")
elif romano=="V":
    print("Vale 5")
elif romano=="X":
    print("Vale 10")
elif romano=="L":
    print("Vale 50")
elif romano=="C":
    print("Vale 100")
elif romano=="D":
    print("Vale 500")
elif romano=="M":
    print("Vale 1000")
else:
    print("No es un numero romano valido")
    
