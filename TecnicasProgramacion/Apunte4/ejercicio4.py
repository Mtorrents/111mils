
romano=str(input("Ingrese un numero romano: "))

if romano=="I":
    print("Valor 1")
elif romano=="V":
    print("Valor 5")
elif romano=="X":
    print("Valor 10")
elif romano=="L":
    print("Valor 50")
elif romano=="C":
    print("Valor 100")
elif romano=="D":
    print("Valor 500")
elif romano=="M":
    print("Valor 1000")
else:
    print("El caracter ingresado no corresponde a un numero romano")
